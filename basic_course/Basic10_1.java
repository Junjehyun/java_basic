    // double 타입의 배열 선언 및 생성
    double [] arr2 = new double [9];
    // 'double[10]'은 10개의 'double'형 데이터를 담을 수 있는 배열을 생성하는 것을 의미한다.

    // 다음은 배열에 값을 저장하거나 값을 가져오는 방법은 다음과 같다.

    // 배열에 값을 저장하는 방법
    // arr[0] = 10;
